import UIKit.UIColor

enum RPEColor {
    static var white: UIColor { UIColor(named: "white") ?? UIColor() }
    static var black: UIColor { UIColor(named: "black") ?? UIColor() }
    static var lightGray: UIColor { UIColor(named: "light-gray") ?? UIColor() }
    static var cobalt: UIColor { UIColor(named: "cobalt") ?? UIColor() }
}
